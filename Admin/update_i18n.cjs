let output = i18nContent;

const insertKeys = (lang) => {
  const marker = new RegExp(`(${lang}:\\s*{\\s*translation:\\s*{)`);
  
  if (output.match(marker)) {
    const keysString = Object.entries(additionalKeys[lang])
      .map(([k, v]) => `      '${k}': '${v.replace(/'/g, "\\'")}',`)
      .join('\\n');
    
    output = output.replace(marker, `$1\n${keysString}`);
  }
};

insertKeys('vi');
insertKeys('en');
insertKeys('sk');

fs.writeFileSync(i18nPath, output);
console.log('Successfully updated i18n.js');
